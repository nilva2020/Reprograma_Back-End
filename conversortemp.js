/* Crie um programa que converta a temperatura
em graus Celsius para Fahrenheit
formula °F = (°C * 9 / 5) + 32 */

// *** Calculo matemático
// valorTGCelsius =  36
// valorFahrenheit = (valorTGCelsius * 9/5 ) + 32

let valorTGCelsius = 36
let valorFahrenheit = (valorTGCelsius * 9/5 ) + 32

console.log(`O valor convertido é: ${valorFahrenheit}`) // interpolar